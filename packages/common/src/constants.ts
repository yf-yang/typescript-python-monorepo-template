/* common used magic numbers */

const MS_PER_SECOND = 1000;
const SECOND_PER_MIN = 60;
const MIN_PER_HR = 60;
const HR_PER_DAY = 24;
export const SECOND = MS_PER_SECOND;
export const MIN = SECOND * SECOND_PER_MIN;
export const HR = MIN * MIN_PER_HR;
export const DAY = HR * HR_PER_DAY;

export const HUNDRED = 100;
export const PERCENTAGES = 100;
